//properties of the string
let a = "Ashwin";
console.log(a.length); // Output: 6 (length of the string)
console.log(a.toUpperCase()); // Output: ASHWIN (converts to uppercase)
console.log(a.toLowerCase()); // Output: ashwin (converts to lowercase) 
console.log(a.indexOf("s")); // Output: 2 (index of the first occurrence of 's')
console.log(a.lastIndexOf("i")); // Output: 4 (index of the last
console.log(a.includes("win")); // Output: true (checks if 'win' is in the string
console.log(a.slice(3,4)); // Output: true (checks if the string starts with 'Ash')


